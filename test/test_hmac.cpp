#include <gtest/gtest.h>
#include <string_view>
#include "shoc/mac/hmac.h"
#include "shoc/hash/sha1.h"
#include "shoc/hash/sha2.h"

using namespace shoc;

struct Data {
    std::string_view key;
    std::string_view msg;
    std::string_view exp;
};

template<class H, size_t N>
void hmac_check(const Data (&test)[N])
{
    Hmac<H> hmac;
    byte bin[H::SIZE];
    char str[H::SIZE * 2 + 1];

    for (auto it : test) {
        hmac(
            it.msg.data(), it.msg.size(), 
            it.key.data(), it.key.size(), 
            bin);
        bin_to_str(bin, sizeof(bin), str, sizeof(str));
        EXPECT_STREQ(it.exp.data(), str);
    }
}

TEST(Hmac, Sha1)
{
    const Data test[] = {
        {   "key",
            "The quick brown fox jumps over the lazy dog",
            "de7c9b85b8b78aa6bc8a7a36f70a90701c9db4d9"  },
    };
    hmac_check<Sha1>(test);
}

